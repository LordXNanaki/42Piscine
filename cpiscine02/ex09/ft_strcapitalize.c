char	ft_strcapitalize(char *str)
{
	int	i;
	int	e;

	e = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if (!((str[e] >= 'a' && str[e] <= 'z')
				|| (str[e] >= '0' && str[e] <= '9')))
		{	
			str[i] = str[i] - 32;
		}	
		i++;
		e = i - 1;
	}
	return (str);
}
