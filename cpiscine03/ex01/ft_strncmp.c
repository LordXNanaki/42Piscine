int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	i;
	int	e;
	int	diff;

	i = 0;
	e = 0;
	diff = 0;
	while (s1[i] != '\0' && i < n)
	{
		i++;
	}
	while (s2[e] != '\0' && e < n)
	{
		e++;
	}
	diff = i - e;
	return (diff);
}
