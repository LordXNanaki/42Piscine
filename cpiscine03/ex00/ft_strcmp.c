int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	e;
	int	diff;

	i = 0;
	e = 0;
	diff = 0;
	while (s1[i] != '\0')
	{
		i++;
	}
	while (s2[e] != '\0')
	{
		e++;
	}
	diff = i - e;
	return (diff);
}
