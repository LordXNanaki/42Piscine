#include <unistd.h>

int	main(int ac, char **ag)
{
	int	i;
	int	k;

	k = 1;
	if (ac == 1)
		return (0);
	while (ag[k] != '\0')
	{
		i = 0;
		while (ag[k][i] != '\0')
		{
			write (1, &ag[k][i], 1);
			i++;
		}
		write (1, "\n", 1);
		k++;
	}
	return (0);
}
